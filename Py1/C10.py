#Pide al usuario que ingrese una frase. Luego, utiliza los métodos upper() y lower() para
#imprimir la frase en mayúsculas y minúsculas, respectivamente.

frase = input("ingrese una frase: ")

mayúsculas = frase.upper()

minisculas = frase.lower()

print("Frase en mayusculas: ",mayúsculas)
print("Frase en minisculas: ",minisculas)

